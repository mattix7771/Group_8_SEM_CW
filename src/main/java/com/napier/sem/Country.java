package com.napier.sem;

/**
 * @author Edgar Park, Mattia Merati and Mateusz Wilczynski
 * Country class represents a country
 * since version 1.0.3
 */


public class Country {




    /**
     * country's  code
     */
    private String code;

    /**
     * country's alternative code
     */
    private String code2;

    /**
     * country's  name
     */
    private String name;

    /**
     * country's  continent
     */
    private String continent;

    /**
     * country's  region
     */
    private String region;

    /**
     * country's  surface Area
     */
    private int surfaceArea;

    /**
     * country's  independence Year
     */
    private int indepYear ;

    /**
     * country's  population
     */
    private int population ;

    /**
     * country's  life Expectancy
     */
    private double lifeExpectancy;

    /**
     * country's  gnp
     */
    private int gnp ;

    /**
     * country's Old gnp
     */
    private int gnpOld ;

    /**
     * country's  capital
     */
    private String capital ;

    /**
     * country's  local Name
     */
    private String localName;

    /**
     * country's  government Form
     */
    private String governmentForm;

    /**
     * country's  head of State
     */
    private String headOfState;



    /**
     * getters and setters
     */
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCode2() {
        return code2;
    }

    public void setCode2(String code2) {
        this.code2 = code2;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContinent() {
        return continent;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public int getSurfaceArea() {
        return surfaceArea;
    }

    public void setSurfaceArea(int surfaceArea) {
        this.surfaceArea = surfaceArea;
    }

    public int getIndepYear() {
        return indepYear;
    }

    public void setIndepYear(int indepYear) {
        this.indepYear = indepYear;
    }

    public int getPopulation() {
        return population;
    }

    public void setPopulation(int population) {
        this.population = population;
    }

    public double getLifeExpectancy() {
        return lifeExpectancy;
    }

    public void setLifeExpectancy(double lifeExpectancy) {
        this.lifeExpectancy = lifeExpectancy;
    }

    public int getGnp() {
        return gnp;
    }

    public void setGnp(int gnp) {
        this.gnp = gnp;
    }

    public int getGnpOld() {
        return gnpOld;
    }

    public void setGnpOld(int gnpOld) {
        this.gnpOld = gnpOld;
    }

    public String getCapital() {
        return capital;
    }

    public void setCapital(String capital) {
        this.capital = capital;
    }

    public String getLocalName() {
        return localName;
    }

    public void setLocalName(String localName) {
        this.localName = localName;
    }

    public String getGovernmentForm() {
        return governmentForm;
    }

    public void setGovernmentForm(String governmentForm) {
        this.governmentForm = governmentForm;
    }

    public String getHeadOfState() {
        return headOfState;
    }

    public void setHeadOfState(String headOfState) {
        this.headOfState = headOfState;
    }
    /**
     * end of getters and setters
     */
}
